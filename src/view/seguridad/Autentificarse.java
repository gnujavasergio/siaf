    public void addEntrar() {
        Usuario usuario = new Usuario();
        usuario.setLogin(this.tfLogin.getText());
        usuario.setPassword(this.pfPassword.getText());

        if (((Usuario) dao.getObject(usuario)).getIdUsuario() != 0) {
            setVisible(false);
            this.screenSize = Toolkit.getDefaultToolkit().getScreenSize();
            this.viewMain = new ViewMain(dao, usuario,conexion);
            this.viewMain.setTitle("Sistema de Activos Fijo - Gobierno Autonomo Municipal de Presto");
            this.viewMain.setSize(screenSize.width, screenSize.height - 30);
            this.viewMain.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this.rootPane, "El usuario no es el correcto", "Mensaje", JOptionPane.ERROR_MESSAGE);
        }
    }

    private class CtrlAutentificarse extends KeyAdapter implements ActionListener {

        private CtrlAutentificarse() {
        }

        @Override
        public void actionPerformed(ActionEvent e) {
            Object aux = e.getSource();
            if (aux.equals(bEntrar)) {
                addEntrar();
            } else if (aux.equals(bSalir)) {
                System.exit(0);
            }
        }

        @Override
        public void keyPressed(KeyEvent e) {
            if (e.getKeyCode() == 10) {
                if (bEntrar.isFocusable()) {
                    addEntrar();
                } else if (pfPassword.isFocusable()) {
                    addEntrar();
                } else if (tfLogin.isFocusable()) {
                    addEntrar();
                }
            }
        }
    }
