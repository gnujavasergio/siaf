    public void setObject(Object obj) {
        this.baja = (Baja) obj;
        if(baja.getActivoFijo().getApiEstado().equals("ELABORADO")){
              bGuardar.setEnabled(false);
        }
        Calendar calendarIncorporacion = Calendar.getInstance();
        calendarIncorporacion.setTime(baja.getActivoFijo().getIncorporacion());
        dccIncorporacion.setSelectedDate(calendarIncorporacion);
        Calendar calendarFechaBaja = Calendar.getInstance();
        calendarFechaBaja.setTime(baja.getFechaBaja());
        dccFechaBaja.setSelectedDate(calendarFechaBaja);
        tfIdActivoFijo.setText(baja.getActivoFijo().getIdActivoFijo());
        taDescripcion.setText(baja.getActivoFijo().getDescripcion());
        tfIndiceUFV.setText(baja.getActivoFijo().getIndiceUFV());
        tfDisposicionRespaldo.setText(baja.getDisposicionRespaldo());
        tfVidaUtil.setText(String.valueOf(baja.getActivoFijo().getVidaUtil()));
        tfValor.setText(String.valueOf(baja.getActivoFijo().getValor()));
        tfPorcentajeDepreciacion.setText(String.valueOf(baja.getActivoFijo().getPorcentajeDepreciacion()));
        tfDepreciacionGestion.setText(String.valueOf(baja.getActivoFijo().getDepreciacionGestion()));
        tfDias.setText(String.valueOf(baja.getActivoFijo().getDias()));
        tfDepreciacionAcumulada.setText(String.valueOf(baja.getActivoFijo().getDepreciacionAcumulada()));
        tfValorNeto.setText(String.valueOf(baja.getActivoFijo().getValorNeto()));
        tfCostoInicial.setText(String.valueOf(baja.getActivoFijo().getCostoInicial()));
        if (baja.getIdBaja() == 0) {
            listMotivoBaja.setSelectedIndex(0);
        } else {
            listMotivoBaja.setSelectedValue(baja.getMotivoBaja(), true);
        }

        taMotivo.setText(baja.getMotivo());
    }

    public void getObject() {
        baja.setFechaBaja(dccFechaBaja.getSelectedDate().getTime());
        baja.setDisposicionRespaldo(tfDisposicionRespaldo.getText());
        baja.setMotivoBaja(listMotivoBaja.getSelectedValue().toString());
        baja.setMotivo(taMotivo.getText());
        baja.setUnidad(ViewMain.viewMain.getUnidad());
        baja.setUsuario(ViewMain.viewMain.getUsuario());
        baja.getActivoFijo().setUsuario(ViewMain.viewMain.getUsuario());
        int option = JOptionPane.showConfirmDialog(ViewMain.viewMain, "Esta seguro de que quiere dar de baja este Activo?", "Mensaje", JOptionPane.YES_NO_OPTION);
        if (option == JOptionPane.YES_OPTION) {
            ViewMain.DAO.add(baja);
            baja.getActivoFijo().setBaja(true);
            ViewMain.DAO.update(baja.getActivoFijo());
            ActivoFijoView.activoFijoView.getActivoFijoTable().reload();
            ActivoFijoView.activoFijoView.getDialog().dispose();
            ActivoFijoView.activoFijoView.getCommonDialog().dispose();
        }
    }

    private class CtrlBajaForm implements ActionListener, FocusListener {

        @Override
        public void actionPerformed(ActionEvent e) {
            Object aux = e.getSource();
            if (aux.equals(bGuardar)) {
                getObject();
            } else if (aux.equals(bCancelar)) {
                ActivoFijoView.activoFijoView.getDialog().dispose();
            } else if (aux.equals(bView)) {
                BajaView.bajaView.getBajaTable().reload();
                ActivoFijoView.activoFijoView.getDialog().dispose();
                ActivoFijoView.activoFijoView.getCommonDialog().dispose();
                ViewMain.viewMain.getFrontend().hide(ViewMain.viewMain.getDockableBaja());
                ViewMain.viewMain.getFrontend().show(ViewMain.viewMain.getDockableBaja());
            }
        }

        @Override
        public void focusGained(FocusEvent e) {
            Object aux = e.getSource();
            if (aux.equals(tfDisposicionRespaldo)) {
                tfDisposicionRespaldo.selectAll();
            } else if (aux.equals(taMotivo)) {
                taMotivo.selectAll();
            }
        }

        @Override
        public void focusLost(FocusEvent e) {
        }
    }
